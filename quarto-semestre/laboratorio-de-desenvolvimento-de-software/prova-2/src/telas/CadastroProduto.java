    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Produto p = new Produto();
        String nome = txtNome.getText();
        float preco = Float.parseFloat(txtPreco.getText());
        float saldo = Float.parseFloat(txtSaldo.getText());
        if (nome.isBlank()) {
            JOptionPane.showMessageDialog(this, "nome vazio");
        }
        else if (preco < 0) {
            JOptionPane.showMessageDialog(this, "preco invalido");
        }
        else if (saldo < 0) {
            JOptionPane.showMessageDialog(this, "saldo invalido");
        }
        else {
            p.setNome(txtNome.getText());
            p.setPreco(Float.parseFloat(txtPreco.getText()));
            p.setSaldo(Float.parseFloat(txtSaldo.getText()));

            ProdutoDAO pDAO = new ProdutoDAO();
            pDAO.inserir(p);
            JOptionPane.showMessageDialog(this, "CADASTRO REALIZADO COM SUCESSO!!!");
            limparFormulario();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    public void limparFormulario(){
        txtNome.setText("");
        txtPreco.setText("");
        txtSaldo.setText("");
    }
    
