    public void preencheTabela() {
        String sexo = "";
        if (radioMasc.isSelected()) {
            sexo = "M";
        }
        else if (radioFem.isSelected()) {
            sexo = "F";
        }
        else sexo = "";
        PessoaDAO pDAO = new PessoaDAO();
        //List<Pessoa> listaPessoas = pDAO.getPessoas();
        List<Pessoa> listaPessoas = pDAO.getPessoasNome(txtPesquisar.getText(), sexo);
        DefaultTableModel tabelaPessoas = (DefaultTableModel) tblPessoas.getModel();
        tabelaPessoas.setNumRows(0);
        for (Pessoa p : listaPessoas) {
            Object[] obj = new Object[]{
                p.getId(),
                p.getNome(),
                p.getSexo(),
                p.getIdioma()};
            tabelaPessoas.addRow(obj);
            }
        }

